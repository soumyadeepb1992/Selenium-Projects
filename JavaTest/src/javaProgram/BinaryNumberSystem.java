package javaProgram;

public class BinaryNumberSystem {
	public static void main(String[] args) 
	{

		int a = 10; // This is not a Binary , it is a Decimal
		System.out.println(a);

		int b = 0b10; 
		System.out.println(b);		      

		int c = 0B11; 
		System.out.println(c);	
		
		int d = 0b1101; 
		System.out.println(d);		

		// int e = 0b1102; 	--> Error
		
	}
}
